<script>
document.addEventListener('DOMContentLoaded', function () {
    // Show the popup after 5 seconds
    setTimeout(function () {
        document.getElementById('popup').classList.add('active');
        startCountdown(24 * 60 * 60); // 24 hours in seconds
    }, 5000);

    // Function to start the countdown
    function startCountdown(duration) {
        let timer = duration;
        const hoursElement = document.getElementById('hours');
        const minutesElement = document.getElementById('minutes');
        const secondsElement = document.getElementById('seconds');

        const interval = setInterval(function () {
            const hours = Math.floor(timer / 3600);
            const minutes = Math.floor((timer % 3600) / 60);
            const seconds = timer % 60;

            hoursElement.textContent = (hours < 10 ? '0' + hours : hours);
            minutesElement.textContent = (minutes < 10 ? '0' + minutes : minutes);
            secondsElement.textContent = (seconds < 10 ? '0' + seconds : seconds);

            if (--timer < 0) {
                clearInterval(interval);
                hoursElement.textContent = "00";
                minutesElement.textContent = "00";
                secondsElement.textContent = "00";
                alert("Time's up!");
            }
        }, 1000);
    }

    // Close the popup
    document.getElementById('closePopup').addEventListener('click', function () {
        document.getElementById('popup').classList.remove('active');
    });
});
</script>

<script>
let currentlyVisibleDetail = null; // Track currently visible detail

function toggleDetails(option) {
    const details = document.querySelectorAll('.container');
    const selectedDetail = document.getElementById(option + '-details');
    
    // If the clicked option is already visible, do not hide it
    if (currentlyVisibleDetail === selectedDetail) {
        return; // Do nothing if the same option is clicked
    }

    details.forEach(detail => {
        detail.style.display = 'none';
    });

    // Show the selected detail
    selectedDetail.style.display = 'block';
    currentlyVisibleDetail = selectedDetail; // Update the currently visible detail
}

// Show the first option's details by default in mobile view
function showDefaultDetails() {
    const details = document.querySelectorAll('.container');
    if (window.innerWidth <= 768) {
        details.forEach(detail => detail.style.display = 'none');
        const firstDetail = document.getElementById('express-details');
        firstDetail.style.display = 'block'; // Show the first option's details
        currentlyVisibleDetail = firstDetail; // Set the first detail as currently visible
    } else {
        details.forEach(detail => detail.style.display = 'block'); // Show all details in PC view
        currentlyVisibleDetail = null; 
    }
}

window.onresize = showDefaultDetails;
showDefaultDetails();

</script>